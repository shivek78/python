# Printing the overview of variables and data types in Python
print("---------------------------------------------------------------------------------------")
print("""
Variables and Data Types in Python
----------------------------------
Variables:
Definition:
A variable in Python is a reserved memory location used to store values. It acts as a container for data and can hold different types of data.
""")
print("---------------------------------------------------------------------------------------")
# Variable storing an integer
age = 25
print("Variable 'age' holds an integer value:", age)
print("Data type of 'age':", type(age))  # Output the type of the variable 'age'
print("---------------------------------------------------------------------------------------")
# Variable storing a floating-point number
temperature = 36.5
print("\nVariable 'temperature' holds a float value:", temperature)
print("Data type of 'temperature':", type(temperature))  # Output the type of the variable 'temperature'
print("---------------------------------------------------------------------------------------")
# Variable storing a string
name = "John"
print("\nVariable 'name' holds a string value:", name)
print("Data type of 'name':", type(name))  # Output the type of the variable 'name'
print("---------------------------------------------------------------------------------------")
# Variable storing a boolean
is_raining = False
print("\nVariable 'is_raining' holds a boolean value:", is_raining)
print("Data type of 'is_raining':", type(is_raining))  # Output the type of the variable 'is_raining'
print("---------------------------------------------------------------------------------------")
print("""
Data Types in Python:
---------------------
Data types in Python specify what kind of value a variable can store. Python has several built-in data types.
Here are some commonly used data types in Python:
1. Integer (int) => Used to store whole numbers, both positive and negative.
2. Float (float) => Used to store decimal numbers.
3. String (str) => Used to store sequences of characters (text).
4. Boolean (bool) => Used to store True or False values.
5. List (list) => Used to store a collection of items.
6. Tuple (tuple) => Used to store immutable collections.
7. Dictionary (dict) => Used to store key-value pairs.
8. Set (set) => Used to store unique values in no particular order.
""")
print("---------------------------------------------------------------------------------------")
# Integer data type example
age = 20
print("\nExample of Integer Data Type - age:", age)
print("Data type of 'age':", type(age))
print("---------------------------------------------------------------------------------------")
# Float data type example
temperature = 36.5
print("\nExample of Float Data Type - temperature:", temperature)
print("Data type of 'temperature':", type(temperature))
print("---------------------------------------------------------------------------------------")
# String data type example
name = "Alice"
print("\nExample of String Data Type - name:", name)
print("Data type of 'name':", type(name))
print("---------------------------------------------------------------------------------------")
# Boolean data type example
is_sunny = True
print("\nExample of Boolean Data Type - is_sunny:", is_sunny)
print("Data type of 'is_sunny':", type(is_sunny))
print("---------------------------------------------------------------------------------------")
# List data type example
fruits = ["apple", "banana", "cherry"]
print("\nExample of List Data Type - fruits:", fruits)
print("Data type of 'fruits':", type(fruits))
print("---------------------------------------------------------------------------------------")
# Tuple data type example
coordinates = (10, 20)
print("\nExample of Tuple Data Type - coordinates:", coordinates)
print("Data type of 'coordinates':", type(coordinates))
print("---------------------------------------------------------------------------------------")
# Dictionary data type example
student = {"name": "John", "age": 21}
print("\nExample of Dictionary Data Type - student:", student)
print("Data type of 'student':", type(student))
print("---------------------------------------------------------------------------------------")
# Set data type example
unique_numbers = {1, 2, 3, 4}
print("\nExample of Set Data Type - unique_numbers:", unique_numbers)
print("Data type of 'unique_numbers':", type(unique_numbers))
print("---------------------------------------------------------------------------------------")